SELECT
ASSET.NAME AS ASSET_NAME,
A10034.NAME AS FIELD_10034,
ASSET_LOCATION.NAME AS LOCATION
FROM 
MACHINE
LEFT JOIN ASSET ON ASSET.MAPPED_ID = MACHINE.ID AND ASSET.ASSET_TYPE_ID=5
LEFT JOIN ASSET_ASSOCIATIONJ10034 ON J10034.ASSET_ID = ASSET.ID AND J10034.ASSET_FIELD_ID=10034
LEFT JOIN ASSET A10034 ON A10034.ID = J10034.ASSOCIATED_ASSET_ID
LEFT JOIN ASSET ASSET_LOCATION ON ASSET_LOCATION.ID = ASET.LOCATION_ID;